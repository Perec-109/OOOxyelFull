window.OOX_CONFIG = {
  WORKER_URL: 'https://ooxyel-contact-proxy.perec.workers.dev',
};

// ---------- Проверка при загрузке страницы ----------
// Если адрес когда-нибудь снова станет плейсхолдером — громко
// предупреждаем в консоли вместо тихого "форма не работает".
(function () {
  const url = window.OOX_CONFIG.WORKER_URL;
  if (!url || url.includes('YOUR-SUBDOMAIN')) {
    console.warn(
      '%c⚠ OOX_CONFIG.WORKER_URL не настроен!',
      'font-size:14px;font-weight:bold;color:#ff8a80;',
      '\nСейчас в config.js стоит плейсхолдер вместо реального адреса Cloudflare Worker.' +
      '\nФорма контактов и заказы не будут доходить до Telegram, пока ты его не заменишь.'
    );
  }
})();
